package com.fantastic.web.vo;

public class Areas {

   private String code;
   private String firstAdd;
   private String lastAdd;
   private String name;
   private int propertice;
   private String memberId;
   
   public String getCode() {
      return code;
   }
   public void setCode(String code) {
      this.code = code;
   }
   public String getFirstAdd() {
      return firstAdd;
   }
   public void setFirstAdd(String firstAdd) {
      this.firstAdd = firstAdd;
   }
   public String getLastAdd() {
      return lastAdd;
   }
   public void setLastAdd(String lastAdd) {
      this.lastAdd = lastAdd;
   }
   public String getName() {
      return name;
   }
   public void setName(String name) {
      this.name = name;
   }
   public int getPropertice() {
      return propertice;
   }
   public void setPropertice(int propertice) {
      this.propertice = propertice;
   }
   public String getMemberId() {
      return memberId;
   }
   public void setMemberId(String memberId) {
      this.memberId = memberId;
   }
   
   
}